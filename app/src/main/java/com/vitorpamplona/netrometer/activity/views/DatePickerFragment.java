package com.vitorpamplona.netrometer.activity.views;

import android.app.DatePickerDialog;
import android.app.Dialog;
import android.app.DialogFragment;
import android.os.Bundle;
import android.widget.DatePicker;

import java.util.Calendar;

public class DatePickerFragment extends DialogFragment
        implements DatePickerDialog.OnDateSetListener {

    DateInputIO io;

    public void setCallbacks(DateInputIO frag) {
        this.io = frag;
    }

    @Override
    public Dialog onCreateDialog(Bundle savedInstanceState) {
        Calendar c = Calendar.getInstance();
        String title = "";

        // Use the current date as the default date in the picker
        if (io != null) {
            c = io.getDefaultDate();
            title = io.getDateTitle();
        }

        int year = c.get(Calendar.YEAR);
        int month = c.get(Calendar.MONTH);
        int day = c.get(Calendar.DAY_OF_MONTH);

        // Create a new instance of DatePickerDialog and return it
        return new FixedTitleDatePickerDialog(getActivity(), this, year, month, day, title);
    }

    public void onDateSet(DatePicker view, int year, int month, int day) {
        if (io != null) { // if the home fragment is not there anymore.
            final Calendar c = Calendar.getInstance();
            c.set(Calendar.YEAR, year);
            c.set(Calendar.MONTH, month);
            c.set(Calendar.DAY_OF_MONTH, day);

            io.onDateChanged(c);
        }
    }

    public static interface DateInputIO {
        public String getDateTitle();
        public Calendar getDefaultDate();
        public void onDateChanged(Calendar c);
    }
}

